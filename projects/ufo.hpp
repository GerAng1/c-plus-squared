#include <iostream>
#include <vector>

void display(int misses);

// Declare variables
std::string codeword = "codecademy", answer = "__________";
int misses = 0;
std::vector<char> incorrect;
bool guess = false;
char letter;

// Define take_turn()
void take_turn() {
  display(misses);

  std::cout << "Incorrect Guesses: " << misses << " {";

  for (int i = 0; i < incorrect.size(); i++) {
    std::cout << incorrect[i] << " ";
  }

  std::cout << "}\n";
  std::cout << "Codeword: ";

  for (int j = 0; j < answer.size(); j++) {
    std::cout << answer[j] << " ";
  }

  std::cout << "\n\n";
  std::cout << "Please enter your guess: ";
  std::cin >> letter;

  for (int k = 0; k < codeword.size(); k++) {
    if (letter == codeword[k]) {
      answer[k] = letter;
      guess = true;
    }
  }

  if (guess == true)
    std::cout << "\nCorrect!\n";
  else {
    std::cout << "Incorrect! The tractor beam pulls the person in further.\n";
    incorrect.push_back(letter);
    misses++;
  }

  guess = false;
}


